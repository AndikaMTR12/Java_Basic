public class Bool {
    public static void main(String[] args) {
        boolean suka = true;
        boolean tidakSuka = false;
        int a = 1, b = 4;
        System.out.println("Saya sangat suka       : " + suka);
        System.out.println("Saya sangat tidak suka : " + tidakSuka);
        System.out.println(a < b);// Hasilnya true karena a = 1 lebih kecil dari b = 4
        System.out.println(a > b);// Hasilnya false karena a = 1 tidak lebih besar dari b = 4
        System.out.println(a == b);// Hasilnya false karena nilai a = 1 tidak sama dengan nilai b = 4;
        System.out.println(a == 1);// Hasilnya true karena nilai a = 1 sama dengan nilai 1

    }
}
